package _training;

public class For {
    public static void main(String[] args) {
        for (int i = 4; i > 0; i--) {
            System.out.println("I ran in " + i + " circles today!");
        }
        for (int i = 3; i < 100; i++) {
            System.out.println("I ran this code " + i + " times.");
        }
        for (int i = 100; i > 30; i--) {
            System.out.println("I have " + i + " minutes left.");
        }
        for (int i = 0; i < 21; i++) {
            System.out.println("I have " + i + " minutes left.");
        }
        int i = 0;
        while(i < 20){
        i++;
            System.out.println("I have " + i + " minutes left.");
        }
    }
}
