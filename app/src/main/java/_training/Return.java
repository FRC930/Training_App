package _training;

public class Return {
    static int square(int num){
        return num*num;
    }
    public static void main(String[] args){
        int square5 = square(5);
        int square2 = square(2);
        System.out.println(square5 + square2);

        int square10 = square(10);
        int square6 = square(6);
        System.out.println(square10 * square6);

        
    }
}
